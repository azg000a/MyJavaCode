package basics2;

import java.util.Scanner;

public class ForLoop {

	public static void main(String[] args) {

		
		
		
		String [] fruits = {"Apple", "Orange", "Mango"};
		for(int i = 0; i < fruits.length; i++) {
			
		
		System.out.println(fruits[i]);
		
		}
		for (String i: fruits) {
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
	}

}
